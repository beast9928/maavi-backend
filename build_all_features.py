import json
from sqlalchemy.orm import Session
from app.models import Invoice

def reconcile_gstr2b(client_id: int, gstr2b_data: dict, db: Session):
    """Match GSTR-2B data against purchase invoices in DB"""
    invoices = db.query(Invoice).filter(
        Invoice.client_id == client_id,
        Invoice.invoice_type == "purchase"
    ).all()
    
    gstr2b_invoices = gstr2b_data.get("data", {}).get("docdata", {}).get("b2b", [])
    
    matched = []
    unmatched_portal = []
    unmatched_books = []
    
    portal_gstin_map = {}
    for supplier in gstr2b_invoices:
        gstin = supplier.get("ctin", "")
        for inv in supplier.get("inv", []):
            key = f"{gstin}_{inv.get('inum','')}"
            portal_gstin_map[key] = {
                "gstin": gstin,
                "invoice_number": inv.get("inum",""),
                "invoice_date": inv.get("idt",""),
                "taxable_value": inv.get("val",0),
                "igst": inv.get("itms",[{}])[0].get("itm_det",{}).get("iamt",0),
                "cgst": inv.get("itms",[{}])[0].get("itm_det",{}).get("camt",0),
                "sgst": inv.get("itms",[{}])[0].get("itm_det",{}).get("samt",0),
            }
    
    books_map = {}
    for inv in invoices:
        if inv.vendor_gstin and inv.invoice_number:
            key = f"{inv.vendor_gstin}_{inv.invoice_number}"
            books_map[key] = inv
    
    all_keys = set(list(portal_gstin_map.keys()) + list(books_map.keys()))
    
    for key in all_keys:
        if key in portal_gstin_map and key in books_map:
            portal = portal_gstin_map[key]
            book = books_map[key]
            diff = abs((portal.get("taxable_value",0) or 0) - (book.taxable_amount or 0))
            matched.append({
                "invoice_number": book.invoice_number,
                "vendor_gstin": book.vendor_gstin,
                "books_amount": book.taxable_amount,
                "portal_amount": portal.get("taxable_value",0),
                "difference": diff,
                "status": "matched" if diff < 1 else "amount_mismatch"
            })
        elif key in portal_gstin_map:
            p = portal_gstin_map[key]
            unmatched_portal.append({
                "invoice_number": p.get("invoice_number"),
                "vendor_gstin": p.get("gstin"),
                "portal_amount": p.get("taxable_value",0),
                "status": "missing_in_books"
            })
        else:
            b = books_map[key]
            unmatched_books.append({
                "invoice_number": b.invoice_number,
                "vendor_gstin": b.vendor_gstin,
                "books_amount": b.taxable_amount,
                "status": "missing_in_portal"
            })
    
    total_itc_available = sum(
        (p.get("igst",0) or 0) + (p.get("cgst",0) or 0) + (p.get("sgst",0) or 0)
        for p in portal_gstin_map.values()
    )
    
    return {
        "total_portal_invoices": len(portal_gstin_map),
        "total_books_invoices": len(books_map),
        "matched": len([m for m in matched if m["status"] == "matched"]),
        "amount_mismatch": len([m for m in matched if m["status"] == "amount_mismatch"]),
        "missing_in_books": len(unmatched_portal),
        "missing_in_portal": len(unmatched_books),
        "total_itc_available": round(total_itc_available, 2),
        "matched_records": matched,
        "unmatched_portal": unmatched_portal,
        "unmatched_books": unmatched_books,
    }
