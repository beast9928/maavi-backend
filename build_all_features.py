# ── Limitation Period Calculator ──────────────────────────────────────────
@extra_router.post("/law/limitation-period")
def calc_limitation(data: dict, u: User = Depends(get_current_user)):
    from datetime import datetime, timedelta
    cause_date_str = data.get("cause_of_action_date")
    case_type = data.get("case_type", "civil")
    if not cause_date_str:
        raise HTTPException(status_code=400, detail="cause_of_action_date required")
    try:
        cause_date = datetime.strptime(cause_date_str, "%Y-%m-%d").date()
    except ValueError:
        raise HTTPException(status_code=400, detail="Invalid date format. Use YYYY-MM-DD")
    limits = {
        "civil": {"years": 3, "act": "Limitation Act 1963 - Article 113"},
        "cheque_bounce": {"years": 0, "days": 30, "act": "NI Act Section 138"},
        "consumer": {"years": 2, "act": "Consumer Protection Act 2019"},
        "labour": {"years": 3, "act": "Industrial Disputes Act"},
        "property": {"years": 12, "act": "Limitation Act 1963 - Article 65"},
        "contract": {"years": 3, "act": "Limitation Act 1963 - Article 55"},
        "tort": {"years": 3, "act": "Limitation Act 1963"},
        "execution": {"years": 12, "act": "Limitation Act 1963 - Article 136"},
    }
    limit = limits.get(case_type, limits["civil"])
    years = limit.get("years", 3)
    extra_days = limit.get("days", 0)
    from dateutil.relativedelta import relativedelta
    try:
        expiry = cause_date + relativedelta(years=years) + timedelta(days=extra_days)
    except Exception:
        expiry = cause_date.replace(year=cause_date.year + years)
    today = date.today()
    days_remaining = (expiry - today).days
    return {
        "cause_of_action_date": str(cause_date),
        "case_type": case_type,
        "limitation_period": f"{years} years" if years else f"{extra_days} days",
        "expiry_date": str(expiry),
        "days_remaining": days_remaining,
        "is_expired": days_remaining < 0,
        "is_urgent": 0 <= days_remaining <= 90,
        "applicable_law": limit.get("act",""),
        "status": "EXPIRED" if days_remaining < 0 else "URGENT" if days_remaining <= 90 else "VALID",
    }

