    purchases = [i for i in invoices if i.invoice_type and i.invoice_type.value == "purchase"]
    total_sales = sum(i.taxable_amount or 0 for i in sales)
    total_purchases = sum(i.taxable_amount or 0 for i in purchases)
    output_gst = sum((i.cgst_amount or 0)+(i.sgst_amount or 0)+(i.igst_amount or 0) for i in sales)
    input_gst = sum((i.cgst_amount or 0)+(i.sgst_amount or 0)+(i.igst_amount or 0) for i in purchases)
    gross_profit = total_sales - total_purchases
    accounts = [
        {"account": "Sales Revenue", "type": "Income", "debit": 0, "credit": round(total_sales, 2)},
        {"account": "Cost of Purchases", "type": "Expense", "debit": round(total_purchases, 2), "credit": 0},
        {"account": "Gross Profit", "type": "Income", "debit": 0, "credit": round(gross_profit, 2)},
        {"account": "Output GST Payable", "type": "Liability", "debit": 0, "credit": round(output_gst, 2)},
        {"account": "Input GST Credit", "type": "Asset", "debit": round(input_gst, 2), "credit": 0},
        {"account": "Net GST Payable", "type": "Liability", "debit": 0, "credit": round(output_gst - input_gst, 2)},
    ]
    total_debit = sum(a["debit"] for a in accounts)
    total_credit = sum(a["credit"] for a in accounts)
    return {
        "client": client.company_name,
        "accounts": accounts,
        "total_debit": round(total_debit, 2),
        "total_credit": round(total_credit, 2),
        "balanced": abs(total_debit - total_credit) < 1,
        "gross_profit": round(gross_profit, 2),
        "net_profit": round(gross_profit, 2),
    }

