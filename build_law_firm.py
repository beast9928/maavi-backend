f.write('def _call(system, user, json_mode=False):\n')
f.write('    if not AI_OK or not client:\n')
f.write('        return json.dumps({"error":"OpenAI API key not configured"}) if json_mode else "OpenAI API key not configured. Add OPENAI_API_KEY to .env"\n')
f.write('    try:\n')
