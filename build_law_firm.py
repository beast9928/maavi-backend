# Helper
f.write('def gen_matter_num(db):\n')
f.write('    count = db.query(func.count(Matter.id)).scalar() or 0\n')
f.write('    return f"MAT-{datetime.now().year}-{count+1:03d}"\n\n')
f.write('def chk_matter(matter_id, user_id, db):\n')
f.write('    m = db.query(Matter).filter(Matter.id==matter_id, Matter.ca_user_id==user_id, Matter.is_active==True).first()\n')
f.write('    if not m: raise HTTPException(status_code=404, detail="Matter not found")\n')
f.write('    return m\n\n')

# Matter routes
f.write('@law_router.get("/matters")\n')
f.write('def list_matters(status: Optional[str]=None, db: Session=Depends(get_db), u: User=Depends(get_current_user)):\n')
f.write('    q = db.query(Matter).filter(Matter.ca_user_id==u.id, Matter.is_active==True)\n')
f.write('    if status: q = q.filter(Matter.status==status)\n')
f.write('    matters = q.order_by(Matter.created_at.desc()).all()\n')
f.write('    return [{"id":m.id,"matter_number":m.matter_number,"title":m.title,"practice_area":m.practice_area,"court":m.court,"status":m.status.value if m.status else "pending","client_name":m.client_name,"opposite_party":m.opposite_party,"next_hearing":str(m.next_hearing) if m.next_hearing else None,"assigned_to":m.assigned_to,"created_at":str(m.created_at)} for m in matters]\n\n')
f.write('@law_router.post("/matters", status_code=201)\n')
f.write('def create_matter(data: MatterCreate, db: Session=Depends(get_db), u: User=Depends(get_current_user)):\n')
f.write('    m = Matter(**data.model_dump(exclude={"status"}), ca_user_id=u.id, matter_number=gen_matter_num(db))\n')
f.write('    if data.status:\n')
f.write('        try: m.status = MatterStatus(data.status)\n')
f.write('        except: m.status = MatterStatus.PENDING\n')
f.write('    db.add(m); db.commit(); db.refresh(m)\n')
f.write('    return {"id":m.id,"matter_number":m.matter_number,"title":m.title,"status":m.status.value}\n\n')
f.write('@law_router.get("/matters/{mid}")\n')
f.write('def get_matter(mid: int, db: Session=Depends(get_db), u: User=Depends(get_current_user)):\n')
f.write('    m = chk_matter(mid, u.id, db)\n')
f.write('    hearings = [{"id":h.id,"hearing_date":str(h.hearing_date),"hearing_time":h.hearing_time,"court":h.court,"purpose":h.purpose,"is_attended":h.is_attended} for h in m.hearings]\n')
f.write('    entries = [{"id":e.id,"entry_date":str(e.entry_date),"hours":e.hours,"rate_per_hour":e.rate_per_hour,"amount":e.amount,"description":e.description,"is_billed":e.is_billed} for e in m.time_entries]\n')
f.write('    return {"id":m.id,"matter_number":m.matter_number,"title":m.title,"practice_area":m.practice_area,"court":m.court,"status":m.status.value if m.status else "pending","client_name":m.client_name,"opposite_party":m.opposite_party,"brief":m.brief,"next_hearing":str(m.next_hearing) if m.next_hearing else None,"filed_date":str(m.filed_date) if m.filed_date else None,"assigned_to":m.assigned_to,"relief_sought":m.relief_sought,"hearings":hearings,"time_entries":entries}\n\n')
f.write('@law_router.put("/matters/{mid}")\n')
f.write('def update_matter(mid: int, data: dict, db: Session=Depends(get_db), u: User=Depends(get_current_user)):\n')
f.write('    m = chk_matter(mid, u.id, db)\n')
f.write('    for k,v in data.items():\n')
f.write('        if k == "status":\n')
f.write('            try: setattr(m, k, MatterStatus(v))\n')
f.write('            except: pass\n')
f.write('        elif hasattr(m, k): setattr(m, k, v)\n')
f.write('    db.commit()\n')
f.write('    return {"updated": True}\n\n')
f.write('@law_router.delete("/matters/{mid}")\n')
f.write('def delete_matter(mid: int, db: Session=Depends(get_db), u: User=Depends(get_current_user)):\n')
f.write('    m = chk_matter(mid, u.id, db)\n')
f.write('    m.is_active = False; db.commit()\n')
f.write('    return {"deleted": True}\n\n')

# Hearing routes
f.write('@law_router.get("/hearings")\n')
f.write('def list_hearings(days: int=30, db: Session=Depends(get_db), u: User=Depends(get_current_user)):\n')
f.write('    from datetime import timedelta\n')
f.write('    today = date.today()\n')
f.write('    deadline = today + timedelta(days=days)\n')
f.write('    matter_ids = [m.id for m in db.query(Matter).filter(Matter.ca_user_id==u.id, Matter.is_active==True).all()]\n')
f.write('    if not matter_ids: return []\n')
f.write('    hs = db.query(CourtHearing).filter(CourtHearing.matter_id.in_(matter_ids), CourtHearing.hearing_date>=today, CourtHearing.hearing_date<=deadline).order_by(CourtHearing.hearing_date).all()\n')
f.write('    result = []\n')
f.write('    for h in hs:\n')
f.write('        m = db.query(Matter).filter(Matter.id==h.matter_id).first()\n')
f.write('        days_left = (h.hearing_date - today).days\n')
f.write('        result.append({"id":h.id,"matter_id":h.matter_id,"matter_number":m.matter_number if m else "","matter_title":m.title if m else "","client_name":m.client_name if m else "","hearing_date":str(h.hearing_date),"hearing_time":h.hearing_time,"court":h.court or (m.court if m else ""),"purpose":h.purpose,"is_attended":h.is_attended,"days_left":days_left,"is_today":days_left==0,"is_urgent":days_left<=3})\n')
f.write('    return result\n\n')
f.write('@law_router.post("/hearings", status_code=201)\n')
