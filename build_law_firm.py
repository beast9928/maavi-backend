f.write('    if m and (not m.next_hearing or h.hearing_date < m.next_hearing):\n')
f.write('        m.next_hearing = h.hearing_date; db.commit()\n')
f.write('    return {"id":h.id,"hearing_date":str(h.hearing_date)}\n\n')
