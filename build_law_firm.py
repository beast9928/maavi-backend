f.write('from datetime import date, datetime\n')
f.write('from app.db.database import get_db\n')
f.write('from app.models.law import Matter, CourtHearing, TimeEntry, LegalDocument, ContractAnalysis, MatterStatus\n')
f.write('from app.models import User\n')
f.write('from app.core.security import get_current_user\n')
