# Email Service
f = open('app/services/email/email_service.py', 'w')
f.write('import smtplib, logging\n')
f.write('from email.mime.text import MIMEText\n')
f.write('from email.mime.multipart import MIMEMultipart\n')
f.write('from datetime import date, timedelta\n')
f.write('from app.core.config import settings\n')
f.write('logger = logging.getLogger(__name__)\n')
f.write('\n')
f.write('def send_email(to_email, subject, body):\n')
f.write('    if not settings.MAIL_USERNAME or not settings.MAIL_PASSWORD:\n')
f.write('        logger.warning("Email not configured")\n')
f.write('        return False\n')
f.write('    try:\n')
f.write('        msg = MIMEMultipart("alternative")\n')
f.write('        msg["Subject"] = subject\n')
f.write('        msg["From"] = settings.MAIL_FROM\n')
f.write('        msg["To"] = to_email\n')
f.write('        msg.attach(MIMEText(body, "html"))\n')
f.write('        with smtplib.SMTP(settings.MAIL_SERVER, settings.MAIL_PORT) as s:\n')
f.write('            s.ehlo(); s.starttls()\n')
f.write('            s.login(settings.MAIL_USERNAME, settings.MAIL_PASSWORD)\n')
f.write('            s.sendmail(settings.MAIL_FROM, to_email, msg.as_string())\n')
f.write('        return True\n')
f.write('    except Exception as e:\n')
f.write('        logger.error(f"Email failed: {e}")\n')
f.write('        return False\n')
f.write('\n')
f.write('def send_compliance_alerts(db):\n')
f.write('    from app.models import ComplianceItem, ComplianceStatus, Client, User\n')
f.write('    today = date.today()\n')
f.write('    sent = 0\n')
f.write('    for client in db.query(Client).filter(Client.is_active==True).all():\n')
f.write('        ca = db.query(User).filter(User.id==client.ca_user_id).first()\n')
f.write('        if not ca or not ca.email: continue\n')
f.write('        items = db.query(ComplianceItem).filter(ComplianceItem.client_id==client.id,ComplianceItem.status.in_([ComplianceStatus.PENDING,ComplianceStatus.OVERDUE]),ComplianceItem.due_date<=today+timedelta(days=7),ComplianceItem.reminder_sent==False).all()\n')
f.write('        if not items: continue\n')
f.write('        body = f"<h2>Compliance Alert for {client.company_name}</h2><p>{len(items)} deadline(s) due within 7 days. Please login to AI Copilot Pro to take action.</p>"\n')
f.write('        if send_email(ca.email, f"Compliance Alert - {client.company_name}", body):\n')
f.write('            for item in items: item.reminder_sent = True\n')
f.write('            db.commit()\n')
f.write('            sent += 1\n')
f.write('    return {"sent": sent}\n')
f.close()
print('Email service created')

